<h3 id="Reforcement_de_l'impression">Premier intérêt : le renforcement de l'impression</h3>
Il est un point crucial dans le développement d'un site web qui est la phase de tests. Si cette dernière est négligée, c'est toute l'application qui risque de se révéler instable dans le temps.
Le présent texte s'inscrit dans cette démarche en proposant un contenu qui bien que dénué de tout intérêt intellectuel, en possède toutefois un d'ordre conceptuel puisqu'il me permet de mettre au jour toutes les subtilités susceptibles d'apparaître de manière inopinée pendant la phase de production.

